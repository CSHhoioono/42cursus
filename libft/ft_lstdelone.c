#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	if (lst->content)
	{
		(*del)(lst->content);
		lst->content = NULL;
	}
	free(lst);
}
